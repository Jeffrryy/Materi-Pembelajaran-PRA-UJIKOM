//function biasa dengan return eksplisit
function tambah(a,b){
    return a+b
}

//function arrow dengan return eksplisit
kali =(c,d) => {
    return c*d
}
//function arrow dengan return implisit
kurang = (e,f) => e-f

fungsi1=tambah(3,4)
arrow1=kali(3,4)
arrow2=kurang(2,1)

console.log(fungsi1)
console.log(arrow1)
console.log(arrow2)